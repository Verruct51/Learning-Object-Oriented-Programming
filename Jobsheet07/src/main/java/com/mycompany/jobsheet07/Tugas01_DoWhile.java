package com.mycompany.jobsheet07;
public class Tugas01_DoWhile {
    public static void main (String[]args){
        String nama = "Alvin Anugerah Pratama";
        int i = 1;
        
        do {
            System.out.println(i + ". " + nama);
            i++;
        }
        while (i <= 100);
    }
}
